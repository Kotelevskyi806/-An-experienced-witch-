styles_order = ["mystic", "sarcastic", "sharp", "toxic"]

def get_tarot_phrase(style):
    return random.choice(tarot_phrases[style])

def get_phrase_from_all():
    all_phrases = []
    for phrases in tarot_phrases.values():
        all_phrases.extend(phrases)
    return random.choice(all_phrases)

def get_phrase_by_count(count):
    if count <= len(styles_order):
        style = styles_order[count - 1]
        return get_tarot_phrase(style)
    return get_phrase_from_all()
